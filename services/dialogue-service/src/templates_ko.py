def get_random_ko_dialogue(event_type: str, emotion: str, persona: str) -> str:
    """
    Get random Korean dialogue for given parameters

    Args:
        event_type: Event type (e.g., "player.victory")
        emotion: Emotion state (e.g., "happy")
        persona: Companion persona (e.g., "cheerful")

    Returns:
        Random dialogue string from templates
    """
    templates = get_dialogue_templates_ko()
    key = (event_type, emotion, persona)

    if key in templates:
        return random.choice(templates[key])

    # Fallback to generic response
    return "..."


# Template statistics
def get_template_stats() -> dict:
    """Get statistics about Korean template library"""
    templates = get_dialogue_templates_ko()

    total_templates = sum(len(dialogues) for dialogues in templates.values())
    event_types = len(set(key[0] for key in templates.keys()))
    emotions = len(set(key[1] for key in templates.keys()))
    personas = len(set(key[2] for key in templates.keys()))

    return {
        "total_templates": total_templates,
        "total_combinations": len(templates),
        "unique_event_types": event_types,
        "unique_emotions": emotions,
        "unique_personas": personas,
    }


